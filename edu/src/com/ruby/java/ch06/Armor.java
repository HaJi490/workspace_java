package com.ruby.java.ch06;

public class Armor {
	private String name;  //private은 같은 class(Armor)안에서만
	private int height;
	
	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

}




